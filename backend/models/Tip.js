const mongoose = require("mongoose");

const tipSchema = new mongoose.Schema(
  {
    userId: {
      type: mongoose.Schema.Types.ObjectId,
      ref: "User",
      required: true,
    },
    title: {
      type: String,
      required: true,
    },
    description: {
      type: String,
      required: true,
    },
  },
  { timestamps: true } // createdAt automatically milega
);

module.exports = mongoose.model("Tip", tipSchema);
 